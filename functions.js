function add7(num){
    let newnum = num + 7;
    return newnum;
}

function multiply(num1,num2){
    multiNum = num1 * num2;
    return multiNum;
}

function capitalize(word){
    const capWord = word.charAt(0).toUpperCase() + word.slice(1);
    return capWord;
}

function lastLetter(word){
    const lastLetter = word.charAt(word.length - 1);
    return lastLetter;
}